const bcrypt = require("bcryptjs");
const jwt = require("jsonwebtoken");
const User = require("../../Model/Auth/user");
const redisClient = require("../../redisClient");
require("dotenv").config();

// üìå Ro‚Äòyxatdan o‚Äòtish (Register)
const register = async (req, res) => {
  try {
    const { username, email, password } = req.body;

    // 1Ô∏è‚É£ Kiruvchi ma'lumotlarni tekshirish
    if (!username || !email || !password) {
      return res.status(400).json({ message: "Barcha maydonlarni to‚Äòldiring!" });
    }

    // 2Ô∏è‚É£ Foydalanuvchi email orqali mavjudligini tekshirish (lowercase email saqlash)
    const existingUser = await User.findOne({ where: { email: email.toLowerCase() } });
    if (existingUser) {
      return res.status(400).json({ message: "Bu email allaqachon ro‚Äòyxatdan o‚Äòtgan." });
    }

    // 3Ô∏è‚É£ Parol uzunligi va xavfsizligini tekshirish
    if (password.length < 6) {
      return res.status(400).json({ message: "Parol kamida 6 ta belgidan iborat bo‚Äòlishi kerak!" });
    }

    // 4Ô∏è‚É£ Parolni shifrlash
    const hashedPassword = await bcrypt.hash(password, 10);

    // 5Ô∏è‚É£ Foydalanuvchini yaratish
    const user = await User.create({
      username,
      email: email.toLowerCase(), // üî• email har doim kichik harflarda saqlanadi
      password: hashedPassword, 
    });

    res.status(201).json({ message: "Foydalanuvchi muvaffaqiyatli ro‚Äòyxatdan o‚Äòtdi!", userId: user.id });

  } catch (error) {
    console.error("Ro‚Äòyxatdan o‚Äòtishda xatolik:", error);
    res.status(500).json({ message: "Server xatosi" });
  }
};

const login = async (req, res) => {
  try {
    const { email, password } = req.body;
    if (!email || !password) {
      return res.status(400).json({ message: "Email va parol kiritilishi shart!" });
    }
    const trimmedEmail = email.trim();
    const trimmedPassword = password.trim();
    console.log("Kelayotgan ma‚Äôlumotlar:", { email: trimmedEmail, password: trimmedPassword });

    let user;
    // 1. Redis keshidan foydalanuvchi ma‚Äôlumotlarini olish
    const cachedUserData = await redisClient.get(`user-data:${trimmedEmail}`);
    
    if (cachedUserData) {
      user = JSON.parse(cachedUserData);
      console.log("Keshdan olingan user:", user);
    } else {
      // Keshda yo‚Äòq bo‚Äòlsa, bazadan olish
      user = await User.findOne({ where: { email: trimmedEmail } });
      if (!user) {
        return res.status(400).json({ message: "Email yoki parol noto‚Äòg‚Äòri!" });
      }
      console.log("Bazadagi user:", { id: user.id, email: user.email, password: user.password });
      
      // Bazadan olingan ma‚Äôlumotni keshga saqlash
      await redisClient.setEx(
        `user-data:${trimmedEmail}`,
        3600,
        JSON.stringify({
          id: user.id,
          email: user.email,
          password: user.password,
        })
      );
    }

    // 2. Parolni tekshirish
    const isMatch = await bcrypt.compare(trimmedPassword, user.password);
    console.log("Parol mosligi:", isMatch);
    if (!isMatch) {
      return res.status(400).json({ message: "Email yoki parol noto‚Äòg‚Äòri!" });
    }

    // 3. Token yaratish
    const token = jwt.sign({ id: user.id }, process.env.JWT_SECRET, { expiresIn: "1h" });

    // 4. Redis‚Äôda tokenni yangilash
    await redisClient.setEx(`user:${user.id}`, 3600, token);
    console.log("Redis‚Äôga saqlangan token:", token);

    // 5. Cookie‚Äôni sozlash
    res.cookie("token", token, {
      httpOnly: true,
      secure: process.env.JWT_SECRET, // Xatolik: process.env.JWT_SECRET o‚Äòrniga NODE_ENV
      sameSite: "Strict",
      maxAge: 60 * 60 * 1000,
    });

    res.status(200).json({ message: "Tizimga muvaffaqiyatli kirdingiz!" });
  } catch (error) {
    console.error("Xatolik:", error.message || error);
    res.status(500).json({ message: "Server xatosi!" });
  }
};
const getProfile = async (req, res) => {
  try {
    const user = await User.findByPk(req.user.id);
    if (!user) {
      return res.status(404).json({ message: "Foydalanuvchi topilmadi" });
    }
    res.json({ id: user.id, email: user.email });
  } catch (error) {
    res.status(500).json({ message: "Server xatosi" });
  }
};

module.exports = { register, login, getProfile };
